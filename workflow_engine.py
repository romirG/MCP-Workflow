def main() -> None:
    import argparse
    import sys

    parser = argparse.ArgumentParser(
        prog="workflow_engine",
        description="Person 3 — Workflow Runtime Engine",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
examples:
  %(prog)s list
  %(prog)s detail server_health_check
  %(prog)s run discover_service_root
  %(prog)s run server_health_check -p SystemId=Server1
  %(prog)s run firmware_update -p ImageURI=http://fw.img -p TargetVersion=2.0
  %(prog)s list --json
        """,
    )

    parser.add_argument(
        "command",
        choices=["list", "detail", "run"],
        help="list = show all workflows · detail = inspect one · run = execute one",
    )
    parser.add_argument("workflow", nargs="?", help="Workflow name (detail/run)")
    parser.add_argument(
        "-f", "--file", default="workflows.yaml", help="Workflows YAML file"
    )
    parser.add_argument(
        "-u", "--url", default="http://localhost:4000", help="API base URL"
    )
    parser.add_argument(
        "-p", "--param", action="append", default=[], help="key=value (repeatable)"
    )
    parser.add_argument(
        "-t", "--timeout", type=int, default=30, help="HTTP timeout (s)"
    )
    parser.add_argument("-v", "--verbose", action="store_true", help="Debug logging")
    parser.add_argument("--json", action="store_true", help="Output as JSON")

    args = parser.parse_args()

    logging.basicConfig(
        level=logging.DEBUG if args.verbose else logging.INFO,
        format="%(message)s",
    )

    try:
        engine = WorkflowEngine(args.file, args.url, args.timeout)
    except Exception as exc:
        print(f"ERROR: {exc}", file=sys.stderr)
        sys.exit(1)

    # -------------------------------------------------------------------
    #  LIST
    # -------------------------------------------------------------------
    if args.command == "list":
        wfs = engine.list_workflows()
        if args.json:
            print(json.dumps(wfs, indent=2))
            return

        print(f"\n{'═' * 72}")
        print(f"  AVAILABLE WORKFLOWS  ({len(wfs)})")
        print(f"{'═' * 72}\n")

        by_cat: Dict[str, list] = {}
        for w in wfs:
            by_cat.setdefault(w["category"] or "other", []).append(w)

        for cat in sorted(by_cat):
            print(f"  ┌─ {cat.upper()}")
            for w in by_cat[cat]:
                params_str = ", ".join(
                    f"{'*' if p['required'] else ''}{p['name']}"
                    for p in w["parameters"]
                )
                print(
                    f"  │  {w['name']:42s}  {w['step_count']:2d} steps  "
                    f"{w['raw_endpoint_count']:3d} endpoints"
                )
                desc = w["description"]
                if len(desc) > 78:
                    desc = desc[:75] + "..."
                print(f"  │    {desc}")
                if params_str:
                    print(f"  │    params: {params_str}")
            print(f"  └{'─' * 70}\n")

    # -------------------------------------------------------------------
    #  DETAIL
    # -------------------------------------------------------------------
    elif args.command == "detail":
        if not args.workflow:
            print("ERROR: workflow name required", file=sys.stderr)
            sys.exit(1)

        detail = engine.get_workflow_detail(args.workflow)
        if detail is None:
            print(f"ERROR: '{args.workflow}' not found", file=sys.stderr)
            sys.exit(1)

        if args.json:
            print(json.dumps(detail, indent=2, default=str))
            return

        print(f"\n{'═' * 72}")
        print(f"  WORKFLOW: {detail['name']}")
        print(f"{'═' * 72}")
        print(f"  Category    : {detail['category']}")
        print(f"  Description : {detail['description']}")

        if detail["parameters"]:
            print(f"\n  PARAMETERS:")
            for p in detail["parameters"]:
                req = "(required)" if p.get("required") else "(optional)"
                dfl = f" [default: {p.get('default')}]" if p.get("default") else ""
                print(f"    {p['name']:20s} {req:12s} {p.get('description', '')}{dfl}")

        print(f"\n  STEPS ({len(detail['steps'])}):")
        for i, s in enumerate(detail["steps"], 1):
            flags = []
            if "condition" in s:
                flags.append("cond")
            if "loop_over" in s:
                flags.append(f"loop:{s['loop_over']}")
            if "break_if" in s:
                flags.append("break_if")
            if "on_error" in s:
                flags.append(f"err:{s['on_error']}")
            tag = f"  [{', '.join(flags)}]" if flags else ""
            print(
                f"    {i:2d}. [{s['step_id']}] "
                f"{s.get('action', 'GET')} {s.get('endpoint', '')}{tag}"
            )
            print(f"        {s.get('description', '')}")

        if detail["raw_endpoints"]:
            print(f"\n  RAW ENDPOINTS ({len(detail['raw_endpoints'])}):")
            for ep in detail["raw_endpoints"]:
                print(f"    • {ep}")

        if detail.get("next_workflows"):
            print(f"\n  SUGGESTED NEXT:")
            for nw in detail["next_workflows"]:
                print(f"    → {nw.get('workflow', '')}  (if {nw.get('if', 'always')})")
                print(f"      {nw.get('reason', '')}")

        if detail.get("output_template"):
            print(f"\n  OUTPUT TEMPLATE:")
            for line in detail["output_template"].split("\n"):
                print(f"    {line}")

        print()

    # -------------------------------------------------------------------
    #  RUN
    # -------------------------------------------------------------------
    elif args.command == "run":
        if not args.workflow:
            print("ERROR: workflow name required", file=sys.stderr)
            sys.exit(1)

        params: Dict[str, str] = {}
        for kv in args.param:
            if "=" in kv:
                k, v = kv.split("=", 1)
                params[k] = v
            else:
                print(f"WARNING: ignoring '{kv}' (use key=value)", file=sys.stderr)

        print(f"\n{'═' * 72}")
        print(f"  ▶ EXECUTING: {args.workflow}")
        print(f"  Server : {args.url}")
        if params:
            print(f"  Params : {params}")
        print(f"{'═' * 72}\n")

        result = engine.run_workflow(args.workflow, params)

        if args.json:
            safe = {
                "workflow_name": result["workflow_name"],
                "success": result["success"],
                "steps_executed": result["steps_executed"],
                "output": result["output"],
                "next_workflows": result["next_workflows"],
                "error": result["error"],
                "started_at": result["started_at"],
                "finished_at": result["finished_at"],
            }
            print(json.dumps(safe, indent=2, default=str))
            return

        print(f"\n{'═' * 72}")
        print(f"  ■ RESULT")
        print(f"{'═' * 72}")
        print(f"  Workflow : {result['workflow_name']}")
        print(f"  Success  : {'✓' if result['success'] else '✗'}")
        print(f"  Steps    : {len(result['steps_executed'])} executed")
        print(f"  Time     : {result['started_at']} → {result['finished_at']}")

        if result["error"]:
            print(f"  Error    : {result['error']}")

        # Step summary
        print(f"\n  STEP LOG:")
        for sr in result["steps_executed"]:
            icon = {"success": "✓", "skipped": "⊘", "error": "✗"}.get(
                sr.get("status", ""), "?"
            )
            extra = ""
            if "status_code" in sr:
                extra = f" [{sr['status_code']}]"
            if "iterations_completed" in sr:
                extra += f" ({sr['iterations_completed']}/{sr['iterations_total']} iters)"
            if sr.get("goto_target"):
                extra += f" → goto:{sr['goto_target']}"
            print(f"    {icon} {sr['step_id']}{extra}")

        if result["output"]:
            print(f"\n  OUTPUT:")
            for line in result["output"].split("\n"):
                print(f"  {line}")

        if result["next_workflows"]:
            print(f"\n  SUGGESTED NEXT:")
            for nw in result["next_workflows"]:
                print(f"    → {nw['workflow']}: {nw['reason']}")

        print(f"\n{'═' * 72}\n")


if __name__ == "__main__":
    main()
